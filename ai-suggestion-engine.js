//////////////////////////////////////////////////////
// SMART ACCOUNT SUGGESTION ENGINE
//////////////////////////////////////////////////////

function suggestAccount(description, type) {

  const desc = (description || "").toLowerCase();

  //////////////////////////////////////////////////////
  // SALARY → PAYROLL ACCOUNT
  //////////////////////////////////////////////////////

  if (desc.includes("salary") || desc.includes("payroll")) {
    return {
      account_code: "5001",
      reason: "Detected payroll transaction"
    };
  }

  //////////////////////////////////////////////////////
  // RENT
  //////////////////////////////////////////////////////

  if (desc.includes("rent")) {
    return {
      account_code: "6001",
      reason: "Detected rent expense"
    };
  }

  //////////////////////////////////////////////////////
  // FUEL
  //////////////////////////////////////////////////////

  if (desc.includes("fuel") || desc.includes("gas")) {
    return {
      account_code: "6002",
      reason: "Transport expense detected"
    };
  }

  //////////////////////////////////////////////////////
  // DEFAULT
  //////////////////////////////////////////////////////

  return {
    account_code: "1000",
    reason: "Default cash/bank mapping"
  };
}
